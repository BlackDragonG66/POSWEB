<?php
session_start();
if (!isset($_SESSION['usuario'])) {
    header('Location: index.php');
    exit();
}
require_once 'conexion.php';
$mensaje = '';
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $nombre = trim($_POST['nombre']);
    $usuario = trim($_POST['usuario']);
    $password = md5($_POST['password']);
    $email = trim($_POST['email']);
    $sql = "INSERT INTO usuarios (nombre, usuario, password, email) VALUES (?, ?, ?, ?)";
    $stmt = $conn->prepare($sql);
    if ($stmt) {
        $stmt->bind_param('ssss', $nombre, $usuario, $password, $email);
        if ($stmt->execute()) {
            $mensaje = 'Usuario registrado correctamente. <a href="index.php">Iniciar sesi√≥n</a>';
        } else {
            $mensaje = 'Error al registrar usuario: ' . $stmt->error;
        }
        $stmt->close();
    } else {
        $mensaje = 'Error en la preparaci√≥n de la consulta: ' . $conn->error;
    }
}
